export interface DayGroup {
  day: number;
  sessions: (Session & { index: number })[];
}

export function getGroupedSchedule(): DayGroup[] {
  const grouped: Record<number, (Session & { index: number })[]> = {};
  quranSchedule.forEach((session, index) => {
    if (!grouped[session.day]) {
      grouped[session.day] = [];
    }
    grouped[session.day].push({ ...session, index });
  });
  return Object.entries(grouped).map(([day, sessions]) => ({
    day: parseInt(day),
    sessions,
  }));
}
